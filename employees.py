def chat_with_employee(employee_id: str, history: list[dict], user_message: str) -> str:
    """指定の社員にメッセージを送り、返答を取得する"""
    emp = get_employee(employee_id)
    if not emp:
        return "指定された社員が見つかりません。"

    messages = []
    for msg in history[-20:]:   # 直近20件のみ送信（トークン節約）
        messages.append({
            "role": "user" if msg["sender"] == "user" else "assistant",
            "content": msg["content"],
        })
    messages.append({"role": "user", "content": user_message})

    try:
        response = client.chat.completions.create(
            model="gpt-4o-mini",
            max_tokens=1024,
            messages=[{"role": "system", "content": emp["system_prompt"]}] + messages,
        )
        return response.choices[0].message.content
    except Exception as e:
        return f"⚠️ エラーが発生しました。APIキーをご確認ください。（{e}）"


