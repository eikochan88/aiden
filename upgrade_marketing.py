# ── スタイルヘルパー ──────────────────────────────────────────

def fill(hex_color):
    h = hex_color.lstrip("#")
    return PatternFill(start_color=h, end_color=h, fill_type="solid")

def border(color="CCCCCC", style="thin"):
    s = Side(style=style, color=color)
    return Border(left=s, right=s, top=s, bottom=s)

def hdr(ws, row, col, value, bg, fg="FFFFFF", size=11, bold=True,
        align="center", wrap=True, colspan=1):
    cell = ws.cell(row=row, column=col, value=value)
    cell.font = Font(name="Meiryo UI", bold=bold, size=size, color=fg.lstrip("#"))
    cell.fill = fill(bg)
    cell.alignment = Alignment(horizontal=align, vertical="center",
                                wrap_text=wrap)
    cell.border = border()
    if colspan > 1:
        ws.merge_cells(
            start_row=row, start_column=col,
            end_row=row, end_column=col + colspan - 1
        )
    return cell

def data(ws, row, col, value, bg="FFFFFF", fg="222222", size=10,
         bold=False, align="left", wrap=True):
    cell = ws.cell(row=row, column=col, value=value)
    cell.font = Font(name="Meiryo UI", bold=bold, size=size, color=fg.lstrip("#"))
    cell.fill = fill(bg)
    cell.alignment = Alignment(horizontal=align, vertical="top", wrap_text=wrap)
    cell.border = border()
    return cell

def title_row(ws, row, text, bg, fg="FFFFFF", ncols=7):
    ws.merge_cells(start_row=row, start_column=1,
                   end_row=row, end_column=ncols)
    c = ws.cell(row=row, column=1, value=text)
    c.font = Font(name="Meiryo UI", bold=True, size=14, color=fg.lstrip("#"))
    c.fill = fill(bg)
    c.alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[row].height = 38

def subtitle_row(ws, row, text, bg, fg="444444", ncols=7):
    ws.merge_cells(start_row=row, start_column=1,
                   end_row=row, end_column=ncols)
    c = ws.cell(row=row, column=1, value=text)
    c.font = Font(name="Meiryo UI", size=10, color=fg.lstrip("#"))
    c.fill = fill(bg)
    c.alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[row].height = 20

def section(ws, row, text, bg, fg="FFFFFF", ncols=7):
    ws.merge_cells(start_row=row, start_column=1,
                   end_row=row, end_column=ncols)
    c = ws.cell(row=row, column=1, value=text)
    c.font = Font(name="Meiryo UI", bold=True, size=11, color=fg.lstrip("#"))
    c.fill = fill(bg)
    c.alignment = Alignment(horizontal="left", vertical="center")
    c.border = border()
    ws.row_dimensions[row].height = 26

def set_cols(ws, col_widths):
    for i, w in enumerate(col_widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = w


